import { useState } from 'react';
import { CheckCircle, AlertCircle, Loader2, ClipboardList } from 'lucide-react';
import { exitSurveyApi } from '../../api/client';
import type { ExitSurveyCreate } from '../../types';
import { PreferredSystem, FatigueLevel } from '../../types';

interface Props {
    participantId: number;
    conditionOrder: 'baseline_first' | 'multiagent_first';
    onComplete: () => void;
}

// Resolve "first"/"second" to the actual condition using the participant's
// assigned order. Stored as preferred_system_actual — directly usable in
// SPSS/R without a JOIN.
function resolvePreference(
    raw: string,
    conditionOrder: 'baseline_first' | 'multiagent_first'
): string {
    if (raw === 'first') return conditionOrder === 'baseline_first' ? 'baseline' : 'multiagent';
    if (raw === 'second') return conditionOrder === 'baseline_first' ? 'multiagent' : 'baseline';
    return raw; // 'no_preference' or 'not_sure' — pass through
}

// ── Debrief text revealed after submission ────────────────────────────────────
const DEBRIEF_TEXT =
    'Thank you for participating! Here is what you experienced: ' +
    'One of the proposals was generated by a single AI acting as a generic senior software architect. ' +
    'The other was produced by three specialised AI agents — Legacy Keeper (stability-focused), ' +
    'Innovator (velocity-focused), and Mediator (pragmatic) — who debated the problem and ' +
    'synthesised a consensus proposal. ' +
    'Half of participants received the single-agent proposal first; the other half received the ' +
    'multi-agent proposal first. Your feedback helps evaluate whether the multi-agent approach ' +
    'produces more trustworthy and actionable architectural proposals.';

// ── Helpers ───────────────────────────────────────────────────────────────────

function LikertScale({
    value,
    onChange,
    lowLabel = 'Very Poor',
    highLabel = 'Excellent',
}: {
    id: string;
    value: string;
    onChange: (v: string) => void;
    lowLabel?: string;
    highLabel?: string;
}) {
    return (
        <div className="space-y-2">
            <div className="flex gap-2">
                {[1, 2, 3, 4, 5, 6, 7].map(n => (
                    <button
                        key={n}
                        type="button"
                        onClick={() => onChange(String(n))}
                        className={`flex-1 py-2 rounded-lg border text-sm font-medium transition-colors ${value === String(n)
                            ? 'bg-cyan-600 border-cyan-600 text-white'
                            : 'bg-white border-slate-200 text-slate-600 hover:border-cyan-400 hover:text-cyan-600'
                            }`}
                    >
                        {n}
                    </button>
                ))}
            </div>
            <div className="flex justify-between text-xs text-slate-400 px-1">
                <span>1 — {lowLabel}</span>
                <span>7 — {highLabel}</span>
            </div>
        </div>
    );
}

// ── Main Component ────────────────────────────────────────────────────────────

export function ExitSurvey({ participantId, conditionOrder, onComplete }: Props) {
    const [responses, setResponses] = useState<Record<string, string>>({});
    const [submitting, setSubmitting] = useState(false);
    const [submitted, setSubmitted] = useState(false);
    const [error, setError] = useState('');

    const set = (key: string, value: string) =>
        setResponses(prev => ({ ...prev, [key]: value }));

    const handleSubmit = async () => {
        // Validate required fields
        const required = [
            'preferred_system',
            'preference_reasoning',
            'interface_rating',
            'experienced_fatigue',
        ];
        const missing = required.filter(f => !responses[f]?.trim());
        if (missing.length > 0) {
            setError('Please answer all required questions before submitting.');
            return;
        }

        setSubmitting(true);
        setError('');

        const payload: ExitSurveyCreate = {
            participant_id: participantId,
            preferred_system: responses.preferred_system as PreferredSystem,
            preferred_system_actual: resolvePreference(responses.preferred_system, conditionOrder),
            preference_reasoning: responses.preference_reasoning.trim(),
            interface_rating: parseInt(responses.interface_rating),
            experienced_fatigue: responses.experienced_fatigue as FatigueLevel,
            technical_issues: responses.technical_issues?.trim() || null,
            additional_feedback: responses.additional_feedback?.trim() || null,
        };

        try {
            await exitSurveyApi.submit(payload);
            setSubmitted(true);
        } catch (err: unknown) {
            const error = err as { response?: { data?: { detail?: unknown } } };
            const msg =
                error?.response?.data?.detail ??
                'Submission failed. Please try again.';
            setError(typeof msg === 'string' ? msg : JSON.stringify(msg));
        } finally {
            setSubmitting(false);
        }
    };

    // ── Post-submission debrief screen ────────────────────────────────────
    if (submitted) {
        return (
            <div className="max-w-2xl mx-auto space-y-6 animate-in fade-in duration-500">
                <div className="p-8 bg-emerald-50 rounded-2xl border border-emerald-100 text-center space-y-4">
                    <CheckCircle size={48} className="mx-auto text-emerald-600" />
                    <h2 className="text-2xl font-bold text-emerald-900">Survey Complete!</h2>
                    <p className="text-emerald-700 leading-relaxed">{DEBRIEF_TEXT}</p>
                </div>
                <div className="text-center">
                    <button
                        onClick={onComplete}
                        className="bg-cyan-600 text-white px-8 py-3 rounded-lg font-bold hover:bg-cyan-700 transition"
                    >
                        Finish
                    </button>
                </div>
            </div>
        );
    }

    // ── Survey form ───────────────────────────────────────────────────────
    return (
        <div className="max-w-2xl mx-auto space-y-6 animate-in fade-in duration-500">
            {/* Header */}
            <div className="flex items-center gap-3 bg-white p-4 rounded-xl border border-slate-200 shadow-sm">
                <div className="p-2 bg-cyan-50 rounded-lg">
                    <ClipboardList size={22} className="text-cyan-600" />
                </div>
                <div>
                    <h2 className="text-lg font-bold text-slate-900">Exit Survey</h2>
                    <p className="text-sm text-slate-500">
                        One final survey before we reveal what you experienced.
                    </p>
                </div>
            </div>

            <div className="bg-white rounded-xl border border-slate-200 shadow-sm divide-y divide-slate-100">

                {/* Q1 — Preferred system */}
                <div className="p-6 space-y-3">
                    <label className="block font-semibold text-slate-800">
                        1. Which AI system did you prefer overall?{' '}
                        <span className="text-red-500">*</span>
                    </label>
                    <p className="text-sm text-slate-500">
                        Based on the quality and trustworthiness of the proposals it generated.
                    </p>
                    <div className="grid grid-cols-1 gap-2 sm:grid-cols-2">
                        {[
                            { value: PreferredSystem.FIRST, label: 'The first AI system you used' },
                            { value: PreferredSystem.SECOND, label: 'The second AI system you used' },
                            { value: PreferredSystem.NO_PREFERENCE, label: 'No preference' },
                            { value: PreferredSystem.NOT_SURE, label: 'Not sure / hard to say' },
                        ].map(opt => (
                            <button
                                key={opt.value}
                                type="button"
                                onClick={() => set('preferred_system', opt.value)}
                                className={`px-4 py-3 rounded-lg border text-sm font-medium text-left transition-colors ${responses.preferred_system === opt.value
                                    ? 'bg-cyan-600 border-cyan-600 text-white'
                                    : 'bg-white border-slate-200 text-slate-700 hover:border-cyan-400 hover:text-cyan-600'
                                    }`}
                            >
                                {opt.label}
                            </button>
                        ))}
                    </div>
                </div>

                {/* Q2 — Preference reasoning */}
                <div className="p-6 space-y-3">
                    <label
                        htmlFor="preference_reasoning"
                        className="block font-semibold text-slate-800"
                    >
                        2. Why did you prefer that system?{' '}
                        <span className="text-red-500">*</span>
                    </label>
                    <textarea
                        id="preference_reasoning"
                        rows={4}
                        value={responses.preference_reasoning ?? ''}
                        onChange={e => set('preference_reasoning', e.target.value)}
                        placeholder="Describe what made that system's proposals more useful, trustworthy, or convincing..."
                        className="w-full border border-slate-200 rounded-lg px-3 py-2 text-sm text-slate-800 placeholder:text-slate-400 focus:outline-none focus:ring-2 focus:ring-cyan-400 resize-y"
                    />
                </div>

                {/* Q3 — Interface rating */}
                <div className="p-6 space-y-3">
                    <label className="block font-semibold text-slate-800">
                        3. How would you rate your overall experience with this interface?{' '}
                        <span className="text-red-500">*</span>
                    </label>
                    <LikertScale
                        id="interface_rating"
                        value={responses.interface_rating ?? ''}
                        onChange={v => set('interface_rating', v)}
                        lowLabel="Very Poor"
                        highLabel="Excellent"
                    />
                </div>

                {/* Q4 — Fatigue */}
                <div className="p-6 space-y-3">
                    <label className="block font-semibold text-slate-800">
                        4. Did you experience fatigue or reduced attention during the study?{' '}
                        <span className="text-red-500">*</span>
                    </label>
                    <div className="flex flex-col gap-2 sm:flex-row">
                        {[
                            { value: FatigueLevel.NONE, label: 'No' },
                            { value: FatigueLevel.A_LITTLE, label: 'A little' },
                            { value: FatigueLevel.YES_SIGNIFICANTLY, label: 'Yes, significantly' },
                        ].map(opt => (
                            <button
                                key={opt.value}
                                type="button"
                                onClick={() => set('experienced_fatigue', opt.value)}
                                className={`flex-1 px-4 py-3 rounded-lg border text-sm font-medium transition-colors ${responses.experienced_fatigue === opt.value
                                    ? 'bg-cyan-600 border-cyan-600 text-white'
                                    : 'bg-white border-slate-200 text-slate-700 hover:border-cyan-400 hover:text-cyan-600'
                                    }`}
                            >
                                {opt.label}
                            </button>
                        ))}
                    </div>
                </div>

                {/* Q5 — Technical issues (optional) */}
                <div className="p-6 space-y-3">
                    <label
                        htmlFor="technical_issues"
                        className="block font-semibold text-slate-800"
                    >
                        5. Did you experience any technical problems?{' '}
                        <span className="text-xs font-normal text-slate-400">(optional)</span>
                    </label>
                    <textarea
                        id="technical_issues"
                        rows={3}
                        value={responses.technical_issues ?? ''}
                        onChange={e => set('technical_issues', e.target.value)}
                        placeholder="E.g. slow loading, errors, layout issues..."
                        className="w-full border border-slate-200 rounded-lg px-3 py-2 text-sm text-slate-800 placeholder:text-slate-400 focus:outline-none focus:ring-2 focus:ring-cyan-400 resize-y"
                    />
                </div>

                {/* Q6 — Additional feedback (optional) */}
                <div className="p-6 space-y-3">
                    <label
                        htmlFor="additional_feedback"
                        className="block font-semibold text-slate-800"
                    >
                        6. Any other comments or feedback?{' '}
                        <span className="text-xs font-normal text-slate-400">(optional)</span>
                    </label>
                    <textarea
                        id="additional_feedback"
                        rows={3}
                        value={responses.additional_feedback ?? ''}
                        onChange={e => set('additional_feedback', e.target.value)}
                        placeholder="Anything else you'd like us to know..."
                        className="w-full border border-slate-200 rounded-lg px-3 py-2 text-sm text-slate-800 placeholder:text-slate-400 focus:outline-none focus:ring-2 focus:ring-cyan-400 resize-y"
                    />
                </div>
            </div>

            {/* Error */}
            {error && (
                <div className="flex items-start gap-3 bg-red-50 border border-red-100 rounded-xl p-4 text-red-700 text-sm">
                    <AlertCircle size={18} className="mt-0.5 flex-shrink-0" />
                    <span>{error}</span>
                </div>
            )}

            {/* Submit */}
            <div className="flex justify-end pb-8">
                <button
                    onClick={handleSubmit}
                    disabled={submitting}
                    className="bg-cyan-600 text-white px-8 py-3 rounded-lg font-bold hover:bg-cyan-700 disabled:opacity-50 disabled:cursor-not-allowed transition flex items-center gap-2"
                >
                    {submitting ? (
                        <>
                            <Loader2 size={18} className="animate-spin" />
                            Submitting…
                        </>
                    ) : (
                        'Submit & See Results'
                    )}
                </button>
            </div>
        </div>
    );
}